// Sélectionne l'élément texte
var te = document.getElementById("temps");

// Ajoute un écouteur d'événements pour détecter le survol de la souris
te.addEventListener("mouseover", function() {
    // Change la couleur du texte au survol
    te.style.color = "red";
});

// Ajoute un écouteur d'événements pour détecter la fin du survol de la souris
te.addEventListener("mouseout", function() {
    // Remet la couleur originale du texte
    te.style.color = "white";
});



